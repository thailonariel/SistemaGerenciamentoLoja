    private void atualizaTabela() {
        dao = new GerenciadorDAO();
        DefaultTableModel TabelaUsu = (DefaultTableModel) tabelaUsuario.getModel();

        ResultSet rs = dao.listarUsuarios();
        try {
            while (rs != null && rs.next()) {
                String nome, login, senha;
                nome = rs.getString("nome");
                login = rs.getString("login");
                senha = rs.getString("senha");

                Object[] novoUsuario = new Object[]{
                    nome,
                    login,
                    senha,};

                TabelaUsu.addRow(novoUsuario);
            }
        } catch (SQLException e) {
            Logger.getLogger(GerenciadorDAO.class.getName()).log(Level.SEVERE, null, e);
        } finally {
            Conexao.fechaConexao();
        }
    }

